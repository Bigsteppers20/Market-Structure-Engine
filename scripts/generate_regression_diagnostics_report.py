"""Runs the Linear Regression Engine's 3-phase diagnostics suite
(``linear_regression.target_diagnostics`` / ``.feature_diagnostics`` /
``.residual_diagnostics``) against real OANDA historical data and writes
the three markdown reports each phase's module docstring promises:

    TARGET_ANALYSIS_REPORT.md    -- Phase 1, per-target statistical audit
    FEATURE_DIAGNOSTICS_REPORT.md -- Phase 2, per-target feature audit
    RESIDUAL_ANALYSIS_REPORT.md  -- Phase 3, per-target residual audit

All three modules were implemented and unit-tested but never run
end-to-end or wired into a report -- this script is that missing step,
mirroring the existing ``scripts/feature_correlation_analysis.py`` pattern
(fetch real candles, compute statistics, write output, never touch a
trained model's point estimate).

Usage (from the project root, with a configured .env -- see
examples/live_oanda_test.py for credential setup)::

    .venv\\Scripts\\python.exe scripts\\generate_regression_diagnostics_report.py
"""
